function arrlist() {
    let arr = [1, 2, 3, 4];
    for (let i = 0; i < arr.length; i++) {
        if (arr.length - 1 > arr.length) {
            return false;
        }
        else if (arr.length - 1 < arr.length) {
            return true
        }
    }
}
